const hasServer = {

  getAdvancedSearchs (query, cb, errorCb) {
    let urlDefault = urladvancedSearchs + '/advancedsearch/' +
      (query.advancedsearchDefaultFilterKey || 'filtertask')
    // get user preference
    let url = endPoint + 'users/' + window.themeDisplay.getUserId() + '/preferences/' +
      (query.preferenceKey || 'FILTERTASK')
    // have to get from user preference
    let queries = {
      start: -1,
      end: -1,
      key: query.advancedsearchDefaultFilterKey || 'filtertask'
    }
    let configApi = {
      headers: {
        'Accept': 'application/json',
        'Content-Type': 'application/json'
      },
      params: Object.assign(queries, query)
    }
    window.axios.all([
      window.axios.get(url, configApi),
      window.axios.get(urlDefault, configApi)
    ])
      .then(window.axios.spread(function (response, response1) {
        let rs = (response.data.value || []).map(item => Object.assign(item, {
          allowDelete: true,
          allowView: true
        }))
        let resultDefault = response1.data.configuration.advancedsearch[0].value
        cb((resultDefault ? window.getEval(resultDefault) : []).concat(rs))
      }))
      .catch(function (error) {
        console.log(error)
        errorCb(error)
      })
  },

  getDictFields (query, cb, errorCb) {
    let url = urladvancedSearchs + '/advancedsearch/' +
      (query.advancedsearchDictKey || 'dictionarytask')
    let queries = {
      start: -1,
      end: -1,
      key: query.advancedsearchDictKey || 'dictionarytask'
    }
    Object.assign(queries, query)
    window.axios.get(url, {
      headers: {
        'Accept': 'application/json',
        'Content-Type': 'application/json'
      },
      params: queries
    })
      .then(function (response) {
        let result = response.data.configuration.advancedsearch[0].value
        let outResult = []
        if (result) {
          cb(window.getEval(result))
        } else {
          cb(outResult)
        }
      })
      .catch(function (error) {
        console.log(error)
        errorCb(error)
      })
  },

  cloneAdvancedSearch (query, cb, errorCb) {
    let url = urladvancedSearchs + '/advancedsearch/' +
      (query.advancedsearchDefaultFilterKey || 'filtertask')
    // have to get from user preference
    let queries = {
      start: -1,
      end: -1,
      key: query.advancedsearchDefaultFilterKey || 'filtertask'
    }
    Object.assign(queries, query)
    window.axios.get(url, {
      headers: {
        'Accept': 'application/json',
        'Content-Type': 'application/json'
      },
      params: queries
    })
      .then(function (response) {
        let result = response.data.configuration.advancedsearch[0].value
        let outResult = []
        if (result) {
          cb(window.getEval(result))
        } else {
          cb(outResult)
        }
      })
      .catch(function (error) {
        console.log(error)
        errorCb(error)
      })
  },

  updateAdvancedSearch (params, cb, errorCb) {
    let urlSearchParams = new URLSearchParams()
    urlSearchParams.append('value', JSON.stringify({value: params.data}))
    const url = endPoint + 'users/' + window.themeDisplay.getUserId() + '/preferences/' +
    (params.preferenceKey || 'FILTERTASK')
    window.axios.put(url, urlSearchParams)
      .then(function (response) {
        cb(response.data.value)
      })
      .catch(function (error) {
        console.log(error)
        errorCb(error)
      })
  }

}

